'''
String de Multiplas Linhas

Para criar uma string de múltiplas linhas, você pode usar três aspas simples ou três aspas duplas.

Exemplo:

'''

# Usando três aspas simples:

mensagem = '''Olá, mundo!
Eu sou uma string de múltiplas linhas.
Python é uma linguagem de programação incrível!'''

print(mensagem)

print('\n')

# Usando três aspas duplas:

mensagem = """Olá, mundo!
Eu sou uma string de múltiplas linhas.
Python é uma linguagem de programação incrível!"""

print(mensagem)

