# Ordem de precedência

x = 10 + 5 * 4
print(x)

x = (10 + 5) * 4
print(x)

x = 10 + 5 * 4 / 3
print(x)

x = 10 + 5 * 4 // 2
print(x)

x = 10 + 5 * 4 % 2
print(x)

