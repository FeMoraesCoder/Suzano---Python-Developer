'''
Operadores de Atribuição

São utilizados para atribuir valores a variáveis.

Operador	Descrição
=	        Atribuição
+=	        Adição
-=	        Subtração
*=	        Multiplicação
/=	        Divisão
//=	        Divisão inteira
%=	        Resto da divisão
**=	        Exponenciação

'''
# %%
#Operadores de atribuição em Python são usados para definir ou atualizar o valor de uma variável. Aqui está uma explicação simples para cada um deles:

print("01) '=' Atribuição simples. Define o valor de uma variável.")

x = 5  # x agora vale 5
print('x = 5 \nx agora vale:', x)
      
print("\n2) '+=' Adição e atribuição. Adiciona um valor à variável e atualiza a variável com o novo valor.")

x += 3  # x agora vale 8 (5 + 3)
print(x)

# -=: Subtração e atribuição. Subtrai um valor da variável e atualiza a variável com o novo valor.

x -= 2  # x agora vale 6 (8 - 2)
print(x)

# *=: Multiplicação e atribuição. Multiplica a variável por um valor e atualiza a variável com o novo valor.

x *= 4  # x agora vale 24 (6 * 4)
print(x)

# //=: Divisão inteira e atribuição. Divide a variável por um valor e atualiza a variável com o valor inteiro da divisão.

x //= 2  # x agora vale 4 (8.0 // 2)
print(x)

# /=: Divisão e atribuição. Divide a variável por um valor e atualiza a variável com o novo valor.

x /= 3  # x agora vale 8.0 (24 / 3)
print(x)

# %=: Módulo e atribuição. Calcula o resto da divisão da variável por um valor e atualiza a variável com o novo valor.

x %= 3  # x agora vale 1 (4 % 3)
print(x)

# **=: Exponenciação e atribuição. Eleva a variável a uma potência e atualiza a variável com o novo valor.

x **= 2  # x agora vale 1 (1 ** 2)
print(x)

# Esses operadores ajudam a simplificar o código, tornando as operações matemáticas e de atualização de variáveis mais concisas.

