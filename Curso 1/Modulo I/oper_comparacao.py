
'''
Operadores de Comparação

São utilizados para comparar valores e retornar um resultado booleano (True ou False).

Operador	Descrição
==	        Igualdade
!=	        Diferente
>	        Maior que
<           Menor que
>=	        Maior ou igual a
<=          Menor ou igual a

 '''
# %%

saldo = 450
saque = 200

print(saldo == saque) # False

print(saldo != saque) # True

print(saldo > saque) # True

print(saldo < saque) # False

print(saldo >= saque) # True

print(saldo <= saque) # False

# %%